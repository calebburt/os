void fb_init(struct limine_framebuffer *framebuffer) {
    fb_global = framebuffer;
}

void unset_pixel(int x, int y) {
    put_pixel(x, y, 0x000000); // Set to black to "unset"
}

void put_pixel(int x, int y, uint32_t color) {
    if (x < 0 || y < 0 || x >= (int)fb_global->width || y >= (int)fb_global->height) return;
    
    volatile uint32_t *fb_ptr = fb_global->address;
    size_t offset = y * (fb_global->pitch / 4) + x;
    fb_ptr[offset] = color;
}

void put_char(int x, int y, char c, uint32_t color) {
    // Handle ASCII characters 32-126 (space to ~)
    if (c < 32 || c > 126) return;
    
    const uint8_t *char_data = &font_bitmap[(c - 32) * 8];
    
    for (int row = 0; row < 8; row++) {
        uint8_t byte = char_data[row];
        for (int col = 0; col < 8; col++) {
            if (byte & (0x80 >> col)) {
                put_pixel(x + col, y + row, color);
            } else {
                unset_pixel(x + col, y + row);
            }
        }
    }
}

void put_string(int x, int y, const char *str, uint32_t color) {
    int curr_x = x;
    while (*str) {
        put_char(curr_x, y, *str, color);
        curr_x += CHAR_WIDTH;
        if (curr_x + CHAR_WIDTH >= (int)fb_global->width) {
            curr_x = x;
            y += CHAR_HEIGHT;
        }
        str++;
    }
}

void print_char(char c, uint32_t color) {
    // Handle control characters
    if (c == '\n') {
        cur.x = 0;
        cur.y += 1;
        return;
    } else if (c == '\r') {
        cur.x = 0;
        return;
    } else if (c == '\t') {
        cur.x += 4 - (cur.x % 4);
        return;
    } else if (c == '\b') {
        if (cur.x > 0) {
            cur.x -= 1;
            print_char(' ', color); // Clear the character
            cur.x -= 1; // Move back again after clearing
        }
        return;
    }
    // Draw character at current cursor position (cur.x is in characters, multiply by 8 for pixels)
    put_char(cur.x * CHAR_WIDTH, cur.y * CHAR_HEIGHT, c, color);
    cur.x += 1;
    // Handle line wrapping
    if ((cur.x * CHAR_WIDTH) + CHAR_WIDTH > (int)fb_global->width) {
        cur.x = 0;
        cur.y += 1;
    }
}

void print_string(const char *str, uint32_t color) {
    while (*str) {
        print_char(*str, color);
        str++;
    }
}


void clear_screen(uint32_t color) {
    for (size_t i = 0; i < fb_global->width * fb_global->height; i++) {
        volatile uint32_t *fb_ptr = fb_global->address;
        fb_ptr[i] = color;
    }
    // Reset cursor to top-left
    cur.x = 0;
    cur.y = 0;
}

void cursor_set(int x, int y) {
    cur.x = x;
    cur.y = y;
}
